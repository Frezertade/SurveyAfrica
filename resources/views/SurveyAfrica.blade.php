                                <strong>Data Accuracy is a very important aspect in data warehousing. While the problem can still persists, companies can have measures to minimize if not eliminate data inaccuracy.</strong>

                                <p>The accuracy and confidence level of a random sample increase with the size of the sample. Even with the low penetration of mobile technology in some african countries, it is far easier to reach the masses through mobile infrastructure than it is through physical interviews. To measure a public metric through random sampling to an error margin of no more than +/- 2% with 99% confidence requires roughly 4000 samples out of a population of 100,000,000. This can be achieved very easily by sending survey requests to a small number of people using their mobile phones.

